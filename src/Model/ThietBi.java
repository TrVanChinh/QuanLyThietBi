public class ThietBi {
    String mTB;
    String mLTB;
    String mTH;
    String tenTB;
    String dvTinh;
    String giaNhap;
    String mKho;
    String soLuong;
    String tinhtrang;
    String slHong;

    public ThietBi() {
    }

    public ThietBi(String mTB, String mLTB, String mTH, String tenTB, String dvTinh, String giaNhap, String mKho, String soLuong, String tinhtrang, String slHong) {
        this.mTB = mTB;
        this.mLTB = mLTB;
        this.mTH = mTH;
        this.tenTB = tenTB;
        this.dvTinh = dvTinh;
        this.giaNhap = giaNhap;
        this.mKho = mKho;
        this.soLuong = soLuong;
        this.tinhtrang = tinhtrang;
        this.slHong = slHong;
    }

    

    public String getmTB() {
        return mTB;
    }

    public void setmTB(String mTB) {
        this.mTB = mTB;
    }

    public ThietBi(String mLTB) {
        this.mLTB = mLTB;
    }

    
    public String getmTH() {
        return mTH;
    }

    public void setmTH(String mTH) {
        this.mTH = mTH;
    }

    public String getTenTB() {
        return tenTB;
    }

    public void setTenTB(String tenTB) {
        this.tenTB = tenTB;
    }

    public String getDvTinh() {
        return dvTinh;
    }

    public void setDvTinh(String dvTinh) {
        this.dvTinh = dvTinh;
    }

    public String getGiaNhap() {
        return giaNhap;
    }

    public void setGiaNhap(String giaNhap) {
        this.giaNhap = giaNhap;
    }

    public String getmKho() {
        return mKho;
    }

    public void setmKho(String mKho) {
        this.mKho = mKho;
    }

    public String getSoLuong() {
        return soLuong;
    }

    public void setSoLuong(String soLuong) {
        this.soLuong = soLuong;
    }

    public String getTinhtrang() {
        return tinhtrang;
    }

    public void setTinhtrang(String tinhtrang) {
        this.tinhtrang = tinhtrang;
    }

    public String getSlHong() {
        return slHong;
    }

    public void setSlHong(String slHong) {
        this.slHong = slHong;
    }

    public String getmLTB() {
        return mLTB;
    }

    public void setmLTB(String mLTB) {
        this.mLTB = mLTB;
    }
    
    
}
